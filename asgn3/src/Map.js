function drawMap() {
    var body = new Cube();
    for (x = 0; x < 32; x++) {
        for (y = 0; y < 32; y++) {
            var textNum = -2;
            var blockheight = g_map[x][y];
            if (g_map[x][y] > 10) {
                // it wants a texture
                textNum = 0;
                blockheight = g_map[x][y] - 20;
            }
            for (height = 0; height < blockheight; height++) {
               body.matrix.setIdentity();
                body.color = [1.0, 1.0, 1.0, 1.0];
                body.matrix.translate(x - 4, -1 + height, y - 4);
                body.textureNum = textNum;
                body.renderFastUV();
            }
        }
    }
}

function drawFloor() {
    var floor = new Cube();
    floor.color = [2, .3, .2, 1];
    floor.matrix.translate(0, -1.0001, 0);
    floor.matrix.scale(100, 0, 100);
    floor.matrix.translate(-.5, 1, -.5);
    floor.render();
}

function drawSky() {
    var sky = new Cube();
    sky.color = [0, 0, 1.0, 1];
    sky.matrix.scale(60, 60, 60);
    sky.matrix.translate(-.5, -.5, -.5);
    sky.render();
}