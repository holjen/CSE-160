var mapSize = 32;
var g_map= [];
function createMap() {
    for (i = 0; i < mapSize; i++) {
        let row = new Array(mapSize).fill(0);
        g_map.push(row);
    }
}
var blocksToPlace = [];
function drawMap() {
    createMap();
    var body = new Cube();
    var wall = new Wall();
    wall.textureN = [-2,-1,0,1];
    wall.height = [0,1,2,3];
    g_map[0][0]= wall;
    for (x = 0; x < 32; x++) {
        for (y = 0; y < 32; y++) {
            if (g_map[x][y] == 0) {
                continue;
            }
            // console.log("x: "+x + " y : "+ y);
            // console.log("map[x][y]: "+g_map[x][y]);
            var wallObj = g_map[x][y]
            for (s = 0; s < wallObj.height.length; s++) {
               body.matrix.setIdentity();
                body.color = [1.0, 1.0, 1.0, 1.0];
                body.matrix.translate((x-2) * 0.75, -1 + wallObj.height[s]* .75, (y-2) * 0.75);
                body.matrix.scale(.75,.75,.75)
                body.textureNum = wallObj.textureN[s];
                body.renderFastUV();
            }
        }
    }
}

function drawFloor() {
    var floor = new Cube();
    floor.color = [2, .3, .2, 1];
    floor.matrix.translate(0, -1.0001, 0);
    floor.matrix.scale(100, 0, 100);
    floor.matrix.translate(-.5, 1, -.5);
    floor.render();
}

function drawSky() {
    var sky = new Cube();
    sky.color = [0, 0, 1.0, 1];
    sky.matrix.scale(60, 60, 60);
    sky.matrix.translate(-.5, -.5, -.5);
    sky.render();
}